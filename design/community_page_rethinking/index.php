              <div class="description_long">
                <p>The idea of establishing the Great Lakes University of Kisumu originated in the Tropical Institute of Community Health and Development (TICH) in Africa, which spearheaded the application for authority to operate as a university. The establishment of TICH was inspired by a number of sources expressing the need for a formal course in Community Based Health Care leading to a recognized academic qualification. Such a course would be offered by an academic institution that could effectively weave together theoretical, evidence based and practical hands-on analytical skills training into development, conducted in learning sites located in the community, civil society, industry and public institutions. </p>
                <p>It was thought that an Institute with the freedom to innovate but linked to an established University would be the best base for such a training program. The Institute was established in 1998 to respond to these challenges and to make a contribution towards the paradigm shift needed for the achievement of sustainable development at various levels particularly in Africa. In this respect, GLUK subscribes to the philosophy of self-propagated development, which is made sustainable through communities' own participation. The idea has attracted some of the best and most renowned brains in the country to nurture the young University to excellence in the fields of health, development and related disciplines. Their involvement ensures the necessary academic rigor while permitting flexibility and responsiveness required in producing graduates that are problem solvers and job creators.</p>
